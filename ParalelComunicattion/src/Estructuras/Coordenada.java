package Estructuras;

/**
 *
 * @author Grupo1
 */
public class Coordenada {
    String x;
    String y;
    
    public Coordenada(String x, String y){
        this.x = x;
        this.y = y;
    }
    
    public String getX(){
        return this.x;
    }
    
    public String getY(){
        return this.y;
    }
}
